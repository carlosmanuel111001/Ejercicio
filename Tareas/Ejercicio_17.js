//17.Escribe una función que tome una cadena de texto como parámetro y
//devuelva la cadena en mayúsculas.

function convertirAMayusculas(cadena) {
    return cadena.toUpperCase();
  }
  
  // Ejemplo de uso
  let texto = "Hola, mundo!";
  let textoMayusculas = convertirAMayusculas(texto);
  console.log(textoMayusculas); // Salida: "HOLA, MUNDO!"
  